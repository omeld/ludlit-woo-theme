		<div class="text rationale ">
			<p class="font-size-2 color-2">Med 30. novembrom in 1. decembrom 2017 bo v Cankarjevem domu, Trubarjevi hiši literature, Vodnikovi domačiji Šiška in Slovenskem gledališkem inštitutu potekal že četrti mednarodni kritiški simpozij, ki bo z mednarodno udeležbo odgovarjal na aktualna vprašanja, povezana z literarno kritiko, in gradil mostove med slovenskimi in tujimi literarnimi kritiki ter uredniki. Gostje iz Avstrije, Češke, Italije, Francije in Slovenije bodo predstavili svoje referate in nastopili na kritiških omizjih, na katerih bodo vzpostavili še kako potreben dialog glede aktualnih kritiških problemov ter vloge literarne kritike v današnjem času.</p>
		</div>
		<div class="text rationale">
<p>Kadar se pogovarjamo o kritiki, se pogosto osredotočamo na njene specifične pojavne oblike (denimo glede na specifike žanrov ali umetniških polj, ki jim pripadajo njeni objekti), pozabimo pa, da pravo izhodišče kritike ni v izbranem delu ali pripadajočem teoretskem zaledju, temveč v kritičnem mišljenju, pristopu, ki si ga pravzaprav delijo vse kritiške prakse. Zato je torej treba bolj kot o smislu samega obstoja kritike vprašati, kako bi lahko širša kritiška praksa stopila v dialog z vedno novimi izzivi svojega zgodovinskega trenutka. Kritika se namreč ne more več zapirati sama vase, temveč se mora vprašati, v kakšnem razmerju je do obstoječih družbenih problemov in premikov.</p>
<p>Toda kako naj se kritika spremeni ali prilagodi, da bo obenem ostala zvesta svojemu izhodišču, ki nujno pomeni tudi določeno distanco in opozicijo? Če je bila kritika tradicionalno razumljena kot konstitutivna praksa in sredstvo razmejevanja (med dobrim in slabim, med posameznimi umetniškimi polji, med ustvarjalcem in kritikom), je dandanes treba ta razmerja na novo premisliti. Hegemonsko vzpostavljanje navidezne avtonomije kritike in njena konstitutivna naloga pri vzpostavljanju homogenosti – in, posledično, diferenciranja – sta, zdi se, nemogoči (nemara celo nezaželeni), zaradi česar je kritika danes soočena z novimi vprašanji – ali naj se namesto zapiranja v izključujoči estetski elitizem posveča strategijam za odpiranje prostora in akceleracionističnim taktikam vključevanja? Si naj prizadeva za vključitev čim več različnih umetniških praks? Naj pomaga k večji slišanosti nerazumljenega in zatiranega, ali pa naj še naprej povzdiguje izključujoči ideal zaključenega in popolnega? Kako naj deluje kot pozitivna ojačitev različnih manjšinskih diskurzov, ne da bi pri tem sama podlegla pastem apropriacije? Kako naj sodeluje pri refleksiji – in pospešitvi – družbenih sprememb?</p>
