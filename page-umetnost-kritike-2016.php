		<div class="text rationale ">
			<p class="font-size-2 color-2">Med 27. in 28. oktobrom 2016 bo v Klubu Cankarjevega doma in v Trubarjevi hiši literature potekal tretji mednarodni kritiški simpozij, ki bo z mednarodno udeležbo odgovarjal na aktualna vprašanja, povezana z literarno kritiko, in gradil mostove med slovenskimi in tujimi literarnimi kritiki ter uredniki. Gostje iz Avstrije, Češke, Hrvaške, Nemčije in Slovenije bodo predstavili svoje referate in nastopili na kritiških omizjih, na katerih bodo vzpostavili še kako potreben dialog glede aktualnih kritiških problemov ter vloge literarne kritike v današnjem času.</p>
		</div>
		<div class="text rationale">
<p>Početje literarnega kritika je videti preprosto: prebrati knjigo in o njej napisati kritiko. Ampak ali je kritikovo delo s tem res zaključeno? Je njegov družbeno-kulturni diskurz zamejen zgolj s številom znakov, ki ga določa naročnik? Kritik s svojim delom namreč stopa v niz dialoških razmerij: z recenziranim tekstom in njegovim avtorjem, z zakonitostmi medija in njegovim urednikom in, ne nazadnje, z bralcem in skupnim družbeno-zgodovinskim trenutkom. Kako mreža teh odnosov določa polje kritikove odgovornosti? Kako se ta spreminja v času novih medijev, blogov in množičnega komentiranja uporabnikov, ki želijo avtorja izvirnega teksta preglasiti s svojim (ne nujno strokovnim) mnenjem? </p>
</p>Ali kritik, tesno vpet v svoje jezikovno in kulturno polje, sploh lahko vstopa v mednarodni družbeno-kulturni dialog? Je kritikova odgovornost v tem, da se zoperstavlja pojmovanju posredovanih resničnosti – tudi literarnih – kot vnaprejšnjega predizbora veljavnih opcij ali pa je njegova odgovornost, nasprotno, predvsem v prepoznavanju in afirmativnem prilagajanju izbrani bralski skupini oziroma medijski platformi? Če je splošno stanje v kulturi takšno, da se v prid hitre potrošnje in tržne uspešnosti izogibamo resnejši refleksiji, kako naj ga kritik potemtakem poskuša, tudi s prevpraševanjem lastne pozicije, spremeniti?</p>

