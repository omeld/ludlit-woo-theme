<div><h3>Onkraj zgodovinjenja. Ob 10. obletnici Prepišnega uredništva (pogovor)</h3>
<p>Dokler pripovedujemo zgodbe, obstajamo, pa čeprav zgolj v zgodbah. Kolikokrat si jih moramo povedati, da jim začnemo še sami verjeti? Spontani prepih osebnih zgodovin in zgodb v desetih spominih. Deset Prepišnih uredništev, tri selitve in kopica pripetljajev v barvi umetne svetlobe. Je sploh pomembno? S pripovedovanjem se prepričujeta in zgodbe z zlatim prahom posuvata Andrej Hočevar in Dražen Dragojević.</p></div>


<div><h3>Večnost je premajhna za žlico (recital)</h3>
<p>Medjezikovne domislice in besedne igre zasedbe Matter – meteor, mem, reperska avantgarda? – lahko z enako legitimnostjo beremo tako na ozadju popkulturnih referenc in raznih modnih znamk kot na podlagi bolj »resne« literature in zbornejše izreke. Če torej besedni del njihove celotne slike označimo za zvrst poezije – kar je skoraj treba, saj nam to sugerirajo z dvema knjižnima izdajama (razprodano Mrak in zadnjo Haos) –, potem ta poezija nedvomno sodi v žanr nizkohermetične poezije z visokimi udarci. Rekli boste, da tak žanr sploh ne obstaja, ampak od Matter naprej je dejstvo. Seveda se je treba najprej navaditi, da v življenju ne moremo vsega razumeti. (Vsaj ne na prvo žogo). In da nam tudi ni treba. Večnost je premajhna za žlico. Tudi Šalamun, če bi bil še živ, bi (re)citiral.</p></div>
