<p>Biti pisateljica je dovolj, preveč in premalo. Pisanje se odziva na svet in ga reflektira, a obenem vanj tudi posega, ga spreminja in postavlja v odnos do množice drugih umišljenih svetov. Ti se lahko dopolnjujejo ali izključujejo. V času hiperprodukcije dobi marsikatero pisanje oznako »literatura«, pri čemer pa ni vedno jasno, ali je to formalna oznaka ali odraz avtorjevega stanja duha, mogoče tudi zgolj bralčeve prizanesljivosti. Kdaj začne pisanje, ki (domnevno? vedno?) črpa iz lastne izkušnje, avtorico presegati, da postanejo meje njenega sveta prepustne? Ne nazadnje, kdo je sploh lahko avtor – trpeča duša ali umetna inteligenca – in zakaj bi nas to sploh moralo zanimati?</p>

<p>Komaj smo ločili avtorja od govorca, že v literaturi ponovno iščemo avtorja, še posebej če ga želimo zaradi njegovega delovanja v resničnem svetu poklicati na odgovornost. Smrt avtorja je bila kratkotrajna. Ko začne avtofikcija premikati meje romana in postavljati nova merila za njegovo presojanje, se moramo vprašati tudi, kje so meje literature. In se ob tem zavedati, da bo odgovor bržkone določil tudi meje naše ideologije. Kajti vse je fikcija in fikcija je vse.</p>
