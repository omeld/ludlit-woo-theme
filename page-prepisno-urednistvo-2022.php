<h2>Podrobnejši opis</h2>

<div class="opisi">
<div><h3>Pisanje nima nobene zveze z nevihto (pogovor)</h3>
<p>Podelitev Nobelove nagrade za književnost primadoni francoske avtobiografske pisave Annie Ernaux je potrdila status avtofikcije kot privilegiranega medija za izražanje deprivilegiranih oziroma dodatno utrdila sloves avtofikcije kot žanra za literarno refleksijo strukturnih neenakostih, ki jih doživljajo ženske, nižji razredi, prišleki in druge skupine. O različnih vidikih avtofikcije se bomo pogovarjali z Dijano Matković, avtorico samorefleksivnega romana Zakaj ne pišem, in Katjo Kobolt, teoretičarko, ki je doktorirala na temo ženske vojne pisave ter naslavlja avtofikcijo tako na področju književnosti kot drugih umetnostnih praks. Pogovor bo vodila Iva Kosmos.</p></div>


<div><h3>10 prepišnih Prišlekov za 10 prepišnih let (branje)</h3>
<p>LUD Literatura na prepih postavlja avtorje in avtorice iz knjižne zbirke Prišleki.</p></div>


<div><h3>Kako (ne) pisati o sebi</h3><p>Srečanje in delavnica o pisanju avtofikcije s Katjo Gorečan.</p>
<p>V začetnem delu srečanja se bomo seznanili z avtofikcijskimi postopki nerojenega otroka kot pripovedovalca v romanu Materinska knjižica. Praktični del pisanja avtofikcije pa se bo posvečal sprehodu skozi zgodbe, ki prehajajo med različnimi svetovi. Kdo pripoveduje in kdo govori, na meji med spomini, premišljevanjem in umetniško domišljijo. Pri tem si bomo pomagali z orodjem kamišibaj kot sredstvom pripovedovanja skozi slike.</p>

