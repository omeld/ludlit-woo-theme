				<h3 id="otvoritev">Otvoritev Prepišnega uredništva. Predstavitev Zamotanih knjig</h3>
				<h4>ponedeljek, 20. 4., ob 19.30</h4>


<p>Zamotane knjige so priložnost, da se povabljeni oblikovalci predstavijo s svojim delom v obliki alternativnih ovitkov za nove knjige. Natisnemo jih v res majhni nakladi in ponudimo kupcem novih knjig v času Prepišnega uredništva – ovitke in knjige se da poljubno kombinirati, tako da ovitek na koncu ni več podrejen knjigi, ampak je knjiga neke vrste nosilec novega umetniškega dela, ki potuje naokoli.</p>
<p>Letošnje zamotane knjige so pripravili <b>Arjan Pregl</b>, <b>Miha Perne</b> in <b>Leon Zuodar</b> <a href="http://www.belisladoled.org">(Beli sladoled)</a>.</p>
<p>Zamotane knjige so samostojna potujoča razstava in hkrati intervencija. So umetniško preigravanje (ne)vrednosti ovojnega papirja, kar je v nekem smislu tudi ščitni ovitek knjige, ki ga lahko po nakupu ali med branjem po želji snameš, celo vržeš proč. So pa Zamotane knjige tudi kritika instrumentaliziranega oblikovanja knjige kot nečesa, kar je podrejeno vsebini, torej tekstu – nov ovitek je lahko tukaj tudi kritika vsebine, vendar kot takšna ponovno podvržena naključju, saj si lahko kupec sam izbere, kateri knjigi bo dodal kateri ovitek. Končno, ne glede na kupčevo domnevno svobodno izbiro svojevrstno zmago slavi prav oblikovalec, ki je svoje delo poslal v svet, razstavil na bralčevi knjižni polici in v nekem smislu prekril ali kar požrl vsebino knjige, ki jo ovijo. Tako ni več vsebina knjige tista, ki bi narekovala oblikovanje, temveč samo še nekakšen narobe obrnjen okvir.</p>
<p>Doslej so sodelovali že <a href="http://minafina.si"><b>Mina Fina</b></a>, <a href="http://blog.ninaurh.com"><b>Nina Urh</b></a>, <b>Miklavž Komelj</b> in <a href="http://www.cizl.si"><b>Nenad Cizl</b></a>.</p>
