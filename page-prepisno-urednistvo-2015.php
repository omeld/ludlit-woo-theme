				<h3 id="pet-do-dvanajst-4">5 do 12: Pogovaranja & Razpravljanja (P & R): Da ne pokvarim</h3>
				<h4>četrtek, 23. 4., ob 11.55</h4>
<p>Pesem, ki je dobro napisana, ima na papirju dobro življenje. Občasno jo posreduješ potencialnim bralcem poslušalcem na literarnih dogodkih. Ko jo glasno bereš, ji daš novo preobleko. Katero obleko izbrati, da začaraš most, ki se vzpostavi med teboj in poslušalcem, in izrečeš skrivno besedo, ki odklene vrata na mostu in je med teboj in poslušalcem pot, ki jo naseli pesem?</p>
<p>Ali dobro pesem sploh lahko pokvarimo? Katera spoznanja s področja nevro-lingvističnega programiranja je dobro vedeti pred nastopanjem? S čim vse komuniciramo, ko izgovarjamo besede?</p>
<p>O tem in še čem v pogovoru dolgoletne koordinatorke Literature v živo Petre Koršič s <b>Tomažem Gubenškom</b>, rednim profesorjem na katedri za govor na oddelku za gledališče in radio AGRFT v Ljubljani, govornim svetovalcem in raziskovalcem govora v slovenskih gledališčih.</p>
