				<h3 id="pet-do-dvanajst-1">5 do 12: »Za topoli nekje si ti«</h3>
				<h4>ponedeljek, 20. 4., ob 11.55</h4>
<p>Pogovor z <strong>Elzo Budau</strong>, slovensko besedilopisko, pesnico in pisateljico, bo vodila Petra Koršič.</p>
<p>Elza Budau je leta 1964 absolvirala na FAGG v Ljubljani in se 1969 zaposlila na RTV Ljubljana. Prozo je začela objavljati 1956 v reviji <em>Mlada pota</em>, med drugim tudi ljubezenski roman <em>Diagram neke ljubezni</em> (1958). Erotično tematiko je nadaljevala tudi v romanu <em>Ljubezen v f-molu</em> (1987). Leta 1963 je pričela pisati besedila za popevke, napisala jih je več kot tisoč.</p>
<p>Elza Budau velja za eno največjih slovenskih besedilopisk. Napisala je besedila za številne pesmi v času razcveta slovenske popevke; nekatere med njimi so: <em>Poletna noč</em>, <em>Brez besed</em>, <em>Samo nasmeh je bolj grenak</em>, <em>Pegasto dekle</em>, <em>Ko ljubezen umre</em>, <em>Na pragu let</em>, <em>Ti si moja ljubezen</em> ... Ko si jih prepevamo, običajno niti ne vemo, kdo jih je spesnil.</p>
<p>Leta 2003 je za svoje življenjsko delo prejela viktorja, leta 2007 pa za svoj radijski in glasbeni opus Ježkovo nagrado.</p>
