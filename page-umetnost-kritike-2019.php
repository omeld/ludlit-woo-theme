		<h1 class="lightred uppercase">Umet&shy;nost kritike 2019</h1>
		<h2 class="uppercase lightred">
			Kritiške transformacije (v 21. stoletju)
		</h2>
		<h3 class="uppercase">Ljubljana, 4. 10. 2019, Trubarjeva hiša literature</h3>
		<h3 class="uppercase">LUD Literatura — Društvo slovenskih literarnih kritikov</h3>
		<p class="intro">V (post?)post-modernem času doživlja kritika preobrazbe, ki jo tako po dosegu (spletna mesta) kot vsebini (neposrednost in subjektivnost) delajo dostopnejšo širšemu občinstvu in v tem smislu demokratizirajo, a jo obenem tudi fragmentarizirajo in vrednotenje bolj kot kdaj koli prej prepuščajo osebnim okusom in merilom. Zdi se, da je umanjkanje trdnega vrednostnega sistema zamajalo temelje kritiškega žanra. Toda – ali je danes tak sistem sploh še mogoče vzpostaviti? Je kritika danes povsem izenačena z mnenjem? Kako je sprememba platforme (prehod s papirja na splet) vplivala na spremembo dveh temeljnih potez kritiškega žanra (vrednotenje in argumentacijo)? Je post-ideološka kritika mit ali realnost? Je prihodnost žanra v njegovi specializaciji (tako kot se že dogaja v glasbi), kjer bi se literarni kritiki ločili na poznavalce detektivk, (znanstvene) fantastike in ljubezenskih romanov?</p>
		<p>Kritiške preobrazbe so neločljivo povezane s spremembami vloge umetnosti in literature v družbi in kot take njena neposredna posledica. Je literatura danes zreducirana na obliko zabave ali pa je to že od nekdaj bila in je zato, zaradi nekonkurenčnosti (beri: neobstoja) drugih medijev, zasedala privilegirano pozicijo? Toda zakaj bi si literatura želela tekmovati s priljubljenostjo, na primer, Netflix serij? Je v svetu deprivilegirane pozicije določenih vej umetnosti kritika obsojena na preobrazbo v PR sporočila?</p>
		<p>Delo kritičarke oziroma kritika je v zadnjih letih doživelo kar nekaj sprememb, a stalnica nemara ostaja predvsem ena – naloga kritike je refleksija umetnosti v družbi.</p>
		<p>Umetnost kritike je že uveljavljeno mednarodno srečanje predvsem literarnih, a tudi drugih kritičark oz. kritikov. Simpozij predstavlja edinstveno priložnost za širšo refleksijo kritike in umetnosti na splošno, s čimer stremi k izboljšanju pogojev kritiškega udejstvovanja in zagotavljanju bolj kulturnega javnega diskurza o umetnosti. Simpozij z mednarodno udeležbo gradi medkulturne mostove in z osveščanjem nemške kulturne javnosti posredno pomaga pri pripravah na nastop Slovenije kot častne gostje v Frankfurtu leta 2022.</p>
