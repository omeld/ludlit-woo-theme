				<h4>torek, 15. 4., ob 16.30</h4>
				<p>Ko je slikar Edgar Degas potožil Stéphanu Mallarméju, da ima toliko idej, kako bi napisal pesmi, pa mu to vseeno ne uspe, mu je le-ta odvrnil: »Dragi moj Degas, pesmi ne delamo z idejami, temveč z besedami.« In vendar, ker smo s Prišleki prišli globoko v gozd besed, nas lahko zanima tudi pot k Degasu: ali je mogoče iz umetniških objektov, ki so jih ustvarili pomeni in zvoki, izrisati tudi podobe. Na delavnici bomo iz izbranih pesemskih besedil izluščili vizualne podobe, ki jih ta besedila priklicujejo in jih poskušali preliti na papir. Iz ustvarjenih podob bo nastala še ena izmed Literaturinih legend, ob koncu tedna Prepišnega uredništva pa bodo izglasovni trije najboljši spoji besede in slike. Prinesite barvice! Vodi <b>Barbara Pogačnik</b>.</p>
