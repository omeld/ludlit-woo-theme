				<h5>predavanje</h5>
				<h4>ponedeljek, 14. 4., ob 18.00</h4>
				<p>Kako se ustvarjalno pisanje razlikuje od pisanja nasploh? Kako začeti? Kako nadaljevati? Ali obstaja prekratka zgodba? Kaj v zgodbi počnejo liki? Od česa so odvisni? Kdo naj zgodbo pripoveduje? Kakšni naj bodo dialogi? Kakšen ritem in zaplet? In navsezadnje, kdo bo vse to sploh bral in kje? Veliko vprašanj, a kar nekaj tudi odgovorov. Če ste kdaj razmišljali o pisanju zgodbe, boste po tem predavanju razmišljali drugače … in svojo zgodbo morda začeli, morda pa končno uspešno končali. In če o pisanju niste nikoli razmišljali, boste o njem v predavanju izvedeli toliko, da boste morda začeli.</p>
				<p><b>Andrej Blatnik</b> je vodja katedre za knjigarstvo na ljubljanski Filozofski fakulteti, urednik in pisatelj z 12 v Sloveniji in 24 v tujini objavljenimi knjigami, zlasti knjigami kratkih zgodb.</p>
