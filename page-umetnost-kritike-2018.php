	<h2>5. mednarodni kritiški simpozij</h2>
	<p style="margin-bottom: 1em; font-size: 1.5em; color: black;">Delo kritičarke oziroma kritika je v zadnjih letih doživelo kar nekaj sprememb, a stalnica nemara ostaja predvsem ena – naloga kritike je refleksija umetnosti v družbi.</p>
	<p><em>Umetnost kritike je že uveljavljeno mednarodno srečanje predvsem literarnih, a tudi drugih kritičark oz. kritikov. Simpozij predstavlja edinstveno priložnost za širšo refleksijo kritike in umetnosti na splošno, s čimer stremi k izboljšanju pogojev kritiškega udejstvovanja in zagotavljanju bolj kulturnega javnega diskurza o umetnosti. Simpozij z mednarodno udeležbo gradi medkulturne mostove in z osveščanjem nemške kulturne javnosti posredno pomaga pri pripravah na nastop Slovenije kot častne gostje v Frankfurtu leta 2022.</em></p>
</section>
<section id="description" class="description">
	<h3>Kritika in javni diskurz</h3>
	<div><p>Kritiški prostor se je v revijalnih, časopisnih in televizijskih medijih skrčil, zato pa je vidno narasel na spletu, kjer ostaja vloga kritike v poplavi kulturne in umetniške produkcije toliko bolj relevantna. Če sklepamo po nastanku številnih novih spletnih revij, blogov, video kritik in esejev, mnenjskih notic in komentarjev, se zdi, da potreba po refleksiji še nikoli ni bila večja. Prav zato je odsotnost kritika v javnem diskurzu, ko gre za kulturo in umetnosti, precej presenetljiv. So se vloge, naloge in vidnost kritike spremenile zaradi predrugačenega položaja umetnosti in kulture v družbi?</p>
	<p>Zdi se, da umetnost še nikoli prej ni bila tako dostopna, njena refleksija pa tako razpršena, podvržena zahtevam trga ter interesov tako avtorjev kot založnikov. Legitimnost kritike se zdi ujeta med nenehnim bojem za pravico do osebnega mnenja in preživetveno PR strategijo.</p>
	<p>Kaj tovrstno preoblikovanje pomeni za poklic kritičarke oziroma kritika? Kako se prilagoditi novemu položaju? Ali pa nam gre tako dobro, da lahko kritiziramo brez kritike? Kako nove okoliščine prilagoditi kritiki v prid? Kako torej s pisanjem kritik (in od njih) živeti, ne le preživeti?</p>
