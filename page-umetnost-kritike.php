		<div class="text rationale ">
			<p class="font-size-2">Med 28. in 29. oktobrom 2015 bo v Klubu Cankarjevega doma potekal drugi mednarodni kritiški simpozij, ki ga organizirajo Javna agencija za knjigo RS, Društvo slovenskih literarnih kritikov, LUD Literatura in Cankarjev dom. Simpozij, na katerem bodo sodelovali uveljavljeni gostje iz Slovenije in tujine, bo odgovarjal na aktualna vprašanja, povezana z literarno kritiko, in gradil mostove med slovenskimi in tujimi literarnimi kritiki ter uredniki. Gostje iz Avstrije, Češke, Hrvaške, Madžarske, ZDA in Slovenije bodo predstavili svoje referate in nastopili na kritiških omizjih, na katerih bodo vzpostavili še kako potreben dialog glede aktualnih kritiških problemov ter vloge literarne kritike v današnjem času.</p>
		</div>
		<div class="text rationale">
			<p>Na simpoziju se bomo posvetili vprašanjem, kot so: kakšna je relevantnost literarne kritike danes? Ali je literarna kritika sposobna – in voljna – generirati vsaj približno enoten kritiški diskurz, ali pa gre zgolj za poplavo naključnih mnenj? Glede na to, da so časi kritiških veličin, ki so praktično lastnoročno krojile obraz literature in vplivale na kulturno-politično klimo v najširšem smislu, nepreklino minili, kako se kritika sooča s svojo novo vlogo? Kakšne so njene transformativne možnosti? Ne nazadnje, kako pomembna sta pri tem kritikova mnenje in slog?</p>
