			<p>Povabili smo pesnike, pisatelje, radijce in kritike. Grafične oblikovalce in ilustratorje. Didžeje. Med drugim bomo premierno predstavili ekskluzivni oblikovalsko-založniški projekt, predstavili nekaj novih knjig, gostili predavanje kreativnega pisanja, prisluhnili branjem in pogovorovom. Na dogodkih bodo skupaj z ožjimi sodelavci LUD Literatura nastopali tudi povabljeni strokovnjaki s področja literature in založništva, avtorji naših knjig in avtorji naših prihodnjih knjig.</p>
