			<p>Na debatah se bomo s strokovnjaki z vseh področij med drugim spraševali, kako intimne izkušnje prehajajo v literarne motive, kako osebno mnenje oblikuje stališča javnosti, kako je ustvarjalnost odvisna od zakonskih uredb.</p>
			<p>V tednu Prepišnega uredništva bodo naše knjige naprodaj v posebnih paketih in po ugodnih sejemskih cenah. Povabili smo pisatelje, psihologe, kolumniste in stripovske ustvarjalce. Predstavili bomo pet novih knjig.</p>
