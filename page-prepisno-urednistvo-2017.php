			<p class="font-size-2">Peti tradicionalni festival bralne kulture Prepišno uredništvo, teden odprtih vrat LUD Literatura, bo posvečen raziskovanju razmerja med zasebnim in javnim v luči bralne kulture. Kako danes, v dobi družbenih medijev, sploh dojemamo meje med zasebnim in javnim – če te seveda sploh še obstajajo?</p><p>Ne samo, da se spreminjata že definiciji, temveč postajata ti nekoč jasno razmejeni polji čedalje bolj prepleteni. Zasebno vpliva na javno, javno vpliva na zasebno: po eni strani javno delovanje določajo zasebni interesi, po drugi pa produkcijski pogoji, rezultat birokratizacije kulturne industrije, vstopajo v intimno delovanje posameznikov na področju kulture. Polemike – nekoč javni dialogi – se odvijajo v krogu istomislečih na družabnih omrežjih, medtem ko se pisatelji pisanja nove knjige lotijo skladno s spremembami zakona o statusu samozaposlenih v kulturi.</p>
