			<p class="font-size-2">Četrto Prepišno uredništvo, teden odprtih vrat LUD Literatura, bo, tako kot jubilejna 300. številka revije Literatura, posvečeno problematiki tiskanih in elektronskih literarnih medijev. Kako bodo videti v prihod­nosti? Kakšno vlogo bodo imeli? Ali sploh imajo prihodnost?</p><p class="font-size-2">Na letošnjem Prepišnem uredništvu bodo v duhu povezovanja prvič soočeni skoraj vsi osrednji slovenski literarni mediji, ki se bodo vzajemno reflektirali. S tem bomo aktivno poskrbeli za večji prepoznavnost in branost slovenskih literarnih medijev, spodbujali bralno kulturo ter kupovanje knjig in revij. Razpisali smo tudi tri natečaje, ki spodbujajo branje, literarno ustvarjalnost in širjenje bralne kulture.</p>
