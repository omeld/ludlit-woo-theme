<p>Na debatah in v tekstih, ki bodo naknadno objavljeni v jubilejni številki revije Literatura, se bomo s strokovnjaki vseh generacij med drugim spraševali, zakaj literarne medije sploh potrebujemo, kdo jih oblikuje, bere in financira, kakšne so njihove produkcijske možnosti, kakšne so razlike med uveljavljenimi in novimi, med tiskanimi in elektronskimi, med tistimi za otroke in tistimi za odrasle.</p>
<p>V tednu Prepišnega uredništva bodo naše knjige naprodaj v posebnih paketih in po ugodnih sejemskih cenah. Povabili smo urednike osrednjih medijev, pisatelje in gledališčnike. Grafične oblikovalce in avtorje najbolj aktualnih kuharskih knjig. Glasbenike. Predstavili bomo tri nove knjige in napovedali posebno številko revije. Na zaključnem dogodku bomo praznovali 20-letnico zbirke Prišleki in uprizorili premierni koncertni performans. Na dogodkih bodo skupaj z ožjimi sodelavci LUD Literatura nastopali tudi povabljeni strokovnjaki s področja literature in založništva, avtorji knjig programa LUD Literatura in avtorji, ki bodo pri LUD Literatura svoje knjige izdali v prihodnje.</p
